import logging
import sqlite3
from flask import Flask, g

DATABASE = 'database.db'

# This defines a Flask application
app = Flask(__name__)


# http://flask.pocoo.org/docs/0.12/patterns/sqlite3/
def get_db():
    db = getattr(g, '_database', None)
    if db is None:
        db = g._database = sqlite3.connect(DATABASE)
    return db


def query_db(query, args=(), one=False):
    cur = get_db().execute(query, args)
    rv = cur.fetchall()
    cur.close()
    return (rv[0] if rv else None) if one else rv


@app.teardown_appcontext
def close_connection(exception):
    db = getattr(g, '_database', None)
    if db is not None:
        db.close()


# Magical annotations define URL routing via the Flask application
@app.route('/')
def hello():
    # Test INSERT and SELECT
    query_db('INSERT INTO mover_reviews VALUES(1, 5, 1)')
    result = query_db('SELECT * FROM mover_reviews')
    return result.__str__()


@app.errorhandler(500)
def server_error(e):
    logging.exception('An error occurred during a request.')
    return """
    An internal error occurred: <pre>{}</pre>
    See logs for full stacktrace.
    """.format(e), 500


# This allows you to run locally.
# When run in GCP, Gunicorn is used instead (see entrypoint in app.yaml) to
# Access the Flack app via WSGI
if __name__ == '__main__':
    app.run(host='127.0.0.1', port=8080, debug=True)
